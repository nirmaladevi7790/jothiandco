 // Close the navbar when the 'About Us' link is clicked
 document.getElementById('aboutus-link').addEventListener('click', function() {
    var navbarToggler = document.querySelector('.navbar-toggler');
    var navbarCollapse = document.querySelector('.navbar-collapse');

    // Check if the navbar is expanded (visible)
    if (navbarCollapse.classList.contains('show')) {
      navbarToggler.click();  // Trigger the toggle to close the menu
    }

    // Scroll to About Us section (if needed)
    document.getElementById('aboutus').scrollIntoView({ behavior: 'smooth' });
  });


